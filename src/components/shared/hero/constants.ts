/**
 * The duration for each character animating in the heading.
 */
export const HEADING_CHARACTER_ANIMATION_DURATION = 0.75;

/**
 * The delay between each character animating in the heading.
 */
export const HEADING_CHARACTER_ANIMATION_STAGGER =
  HEADING_CHARACTER_ANIMATION_DURATION * 0.05;

/**
 * The duration after the heading has animated in before the subheading starts
 * animating.
 */
export const SUBHEADING_DELAY = 0.2;

/**
 * The duration for each line animating in the subheading.
 */
export const SUBHEADING_LINE_ANIMATION_DURATION = 0.35;

/**
 * The duration between each line animating in the subheading.
 */
export const SUBHEADING_LINE_STAGGER =
  0.75 * SUBHEADING_LINE_ANIMATION_DURATION;
